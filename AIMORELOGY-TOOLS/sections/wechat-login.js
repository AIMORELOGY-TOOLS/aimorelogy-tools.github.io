    // ÂàõÂª∫‰∫åÁª¥Á†Å
    async createQR() {
        try {
            this.showStatus('Ê≠£Âú®ÁîüÊàê‰∫åÁª¥Á†Å...');
            
            const response = await fetch(`${this.config.apiBaseUrl}/create_qr`, {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json',
                }
            });

            if (!response.ok) {
                throw new Error(`HTTP ${response.status}: ${response.statusText}`);
            }

            const data = await response.json();
            
            if (data.success) {
                this.sessionId = data.sessionId;
                this.showQR(data.qrUrl);
                this.startPolling();
                this.startExpireTimer(data.expireSeconds || 600);
                this.showStatus('ËØ∑‰ΩøÁî®ÂæÆ‰ø°Êâ´Êèè‰∫åÁª¥Á†Å');
            } else {
                throw new Error(data.error || 'ÁîüÊàê‰∫åÁª¥Á†ÅÂ§±Ë¥•');
            }
        } catch (error) {
            console.error('ÂàõÂª∫‰∫åÁª¥Á†ÅÂ§±Ë¥•:', error);
            this.showError('ÁîüÊàê‰∫åÁª¥Á†ÅÂ§±Ë¥•ÔºåËØ∑ÈáçËØï');
        }
    }

