    async incrementUsage(featureType = 'default') {
        if (!this.currentUser) {
            throw new Error('Áî®Êà∑Êú™ÁôªÂΩï');
        }
        
        try {
            const response = await fetch(`${this.config.apiBaseUrl}/update_usage`, {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json',
                    'Authorization': `Bearer ${this.currentUser.token}`
                },
                body: JSON.stringify({
                    token: this.currentUser.token,
                    action: featureType,
                    amount: 1
                })
            });
            
            const data = await response.json();
            
            if (data.success) {
                // Êõ¥Êñ∞Êú¨Âú∞Áî®Êà∑‰ø°ÊÅØ
                this.currentUser.usage = data.usage;
                localStorage.setItem(this.config.storageKey, JSON.stringify(this.currentUser));
                
                // Êõ¥Êñ∞UIÊòæÁ§∫
                if (this.container) {
                    this.render(this.container);
                }
                
                return data;
            } else {
                throw new Error(data.error || 'Êõ¥Êñ∞‰ΩøÁî®Ê¨°Êï∞Â§±Ë¥•');
            }
        } catch (error) {
            console.error('Â¢ûÂä†‰ΩøÁî®Ê¨°Êï∞Â§±Ë¥•:', error);
            throw error;
        }
    }

